<script>
document.addEventListener("DOMContentLoaded", function() {
    var tabs = document.querySelectorAll('.tabLink');
    var closeBtns = document.querySelectorAll('.gallery-trigger-close');

    tabs.forEach(function(tab) {
      tab.addEventListener('click', function(e) {
        e.preventDefault();
        var tabId = this.getAttribute('data-tab');
        var tabContent = document.getElementById(tabId + 'Tab');
        var parentLi = this.parentElement;
        if (tabContent.style.height === 'auto') {
          tabContent.classList.remove('active');
          parentLi.classList.remove('active');
        } else {
          hideAllTabs();
          tabContent.classList.add('active');
          parentLi.classList.add('active');
        }
      });
    });

    function hideAllTabs() {
      var tabContents = document.querySelectorAll('.clickItem');
      var tabs = document.querySelectorAll('.tabLink');
      tabContents.forEach(function(content) {
        content.classList.remove('active');
      });

      tabs.forEach(function(tab) {
        tab.classList.remove('active');
      });
    }

   closeBtns.forEach(function(closeBtn) {
      closeBtn.addEventListener('click', function() {
        hideAllTabs();
      });
    });


    // Add scroll event listener to close active tabs on scroll
    window.addEventListener('scroll', function() {
      hideAllTabs();
    });
});
 
</script>
