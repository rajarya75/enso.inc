    const collapsibleButtons = document.querySelectorAll(
            ".collapsible-trigger-btn"
        );

        collapsibleButtons.forEach((collapsibleButton) => {
            collapsibleButton.addEventListener("click", (e) => {
                const currentTab = e.currentTarget.parentElement;
                const isOpen = currentTab.classList.contains("collapsible-tab__open");

                // Close all tabs
                document.querySelectorAll(".collapsibles-wrapper").forEach((tab) => {
                    tab.classList.remove("collapsible-tab__open");
                    tab.querySelector(".collapsible-content").style.maxHeight = "0";
                });

                // If the clicked tab was closed, open it
                if (!isOpen) {
                    currentTab.classList.add("collapsible-tab__open");
                    const content = currentTab.querySelector(".collapsible-content");
                    content.style.maxHeight = content.scrollHeight + "px";
                }
            });
        });
