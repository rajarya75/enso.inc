		// Function to generate slug from text
		function generate_slug($text)
		{
			// Convert to lowercase
			$text = strtolower($text);

			// Replace spaces with hyphens
			$text = str_replace(' ', '-', $text);

			// Remove special characters except hyphens
			$text = preg_replace('/[^a-z0-9-]/', '', $text);

			// Remove leading and trailing hyphens
			$text = trim($text, '-');

			return $text;
		}

		// Execute the SQL query with post meta table join
		$query = "SELECT SQL_CALC_FOUND_ROWS wp_posts.*, MAX(meta.meta_value) AS featured_image_id
		FROM wp_posts
		LEFT JOIN wp_term_relationships ON (wp_posts.ID = wp_term_relationships.object_id)
		LEFT JOIN wp_postmeta AS meta ON (wp_posts.ID = meta.post_id AND meta.meta_key = '_thumbnail_id')
		WHERE 1=1  
		AND (wp_term_relationships.term_taxonomy_id IN (2))
		AND ((wp_posts.post_type = 'post' AND (wp_posts.post_status = 'publish' OR wp_posts.post_status = 'acf-disabled')))
		GROUP BY wp_posts.ID, wp_posts.post_title, wp_posts.post_date
		ORDER BY wp_posts.post_date DESC
		LIMIT 0, 5;";

		$result = mysqli_query($connection, $query);

		// Check for errors
		if (!$result) {
			die("Query failed: " . mysqli_error($connection));
		}

		// Check if there are any results
		if (mysqli_num_rows($result) > 0) {
